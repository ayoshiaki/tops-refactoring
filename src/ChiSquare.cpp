  int chiPi(double p) {
    double prob[] = {0.975, 0.2, 0.1, 0.05, 0.025, 0.01, 0.005, 0.001, 0.0005};
    int pi = 0;
    for (int i = 0; i < 9; i++) {
      if (p >= prob[i]) {
        if (i == 0)
          pi = 0;
        else if (abs(p - prob[i]) > abs(p - prob[i-1]))
          pi = i-1;
        else
          pi = i;
        break;
      }
    }
    return pi;
  }

  int chiDfi(int df) {
    if (df <= 25)
      return df-1;

    int dfi = 30;
    while ((df - dfi) > 0) {
      dfi += 10;
      if (dfi == 80) {
        if (abs(df - 80) > abs(df - 100))
          return 30;
        else
          return 29;
      }
    }
    return ((dfi - 30)/10 + 25);
  }

  double chiSquare(double p, int df) {
    int pi = chiPi(p);
    int dfi = chiDfi(df);
    return CHI_QUARE[dfi][pi];
  }
